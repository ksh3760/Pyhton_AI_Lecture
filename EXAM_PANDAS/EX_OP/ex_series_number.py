# 모듈 로딩 -------------------------------------
import pandas as pd

# 데이터 준비 ------------------------------------
# 4가지 과목 국어, 영어, 수학, 과학 점수 데이터를 Series객체로 저장
score = pd.Series({'국어': 100, '영어':87, '수학': 98, '과학': 67})
print(f"score => {score}")
print(f"score.index => {score.index}")
print(f"score.value => {score.value}")

